from pathlib import Path
import sys

import pandas as pd
import streamlit as st

project_root = Path(__file__).resolve().parents[1]
sys.path.append(str(project_root))

st.set_page_config(
    page_title="Диагностика СВЧ-модулей",
    page_icon="⚙️",
    layout="wide"
)

st.title("ML-система предиктивной диагностики СВЧ-модулей")

st.markdown("""
Система анализирует температурные и вибрационные временные ряды, определяет состояние СВЧ-модулей
и формирует предупреждения для оператора и план проверки для техника.
""")

st.subheader("Назначение системы")

st.markdown("""
1. Отслеживать состояние СВЧ-модулей на испытательных стендах.
2. Выявлять перегрев, ухудшение охлаждения и вибрационные неисправности.
3. Показывать оператору уровень риска.
4. Формировать список модулей, которые нужно проверить технику.
""")

st.subheader("Логика работы")

st.code("""
Датчики температуры и вибрации
        ↓
Окна сигнала по 5 секунд
        ↓
Временные и частотные признаки
        ↓
ML-модель диагностики
        ↓
Оценка риска
        ↓
Панель оператора и план проверки техника
""", language="text")

st.subheader("Результат работы системы")

example = pd.DataFrame(
    {
        "Модуль": ["СВЧ-модуль №014"],
        "Стенд": ["Испытательный стенд №03"],
        "Состояние": ["Ухудшение охлаждения"],
        "Уровень риска": ["🟠 Высокий"],
        "Рекомендация": ["Проверить радиатор, термопрокладку и вентиляционные отверстия"]
    }
)

st.dataframe(example, use_container_width=True)
